// Normalize a label for lookup: lowercase, trim, collapse whitespace, strip trailing punctuation
function normalize(text: string): string {
  return text
    .toLowerCase()
    .trim()
    .replace(/\s+/g, ' ')
    .replace(/[*]+$/, '')
    .trim();
}

const dictionary = new Map<string, string>(
  RAW.map(([key, value]) => [normalize(key), value])
);

export function lookupFieldName(text: string): string | undefined {
  return dictionary.get(normalize(text));
}
